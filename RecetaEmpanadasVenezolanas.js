// Receta para hacer empanadas venezolanas.
console.log("¡Vamos a preparar unas deliciosas empanadas venezolanas!");


// Lista de ingredientes.
console.log("Ingredientes:");
console.log("Harina de maiz: 2 tazas."); // Harina de maíz precocida (marca P.A.N. es común en Venezuela).
console.log("Agua: 1 - 1/2 tazas."); // Agua tibia.
console.log("Sal: 1 cucharadita."); // Sal al gusto.
console.log("Relleno: Pollo, carne, jamón, queso, platano, cazón."); // El relleno puede ser cualquiera, a tu gusto.
console.log("Aceite: 500 mL o 1L."); // Aceite de girasol o de oliva.
console.log("Ingrediente secreto: 1 cucharadita de azúcar."); // Esto ayuda a darle un toque único a la masa.

/* 
Paso a paso para preparar la masa de las empanadas 
y armarlas antes de freírlas.
*/

console.log("Preparando la masa:");
// Mezclar los ingredientes secos.
console.log("1. Mezcla la harina, sal y el azúcar en un bowl grande.");

// Agregar el agua tibia poco a poco.
console.log("2. Agrega el agua tibia poco a poco mientras amasas.");
console.log("La masa está lista para usarse.");
    
console.log("Armando las empanadas:");
/* 
Aquí, se divide la masa en bolas individuales, 
se extiende con las manos y se agrega el relleno.
*/
console.log("3. Divide la masa en porciones y haz bolitas.");
console.log("4. Extiende cada bolita en un círculo.");
console.log("5. Agrega el relleno en el centro del círculo.");
console.log("6. Cierra la masa en forma de media luna y sella los bordes.");

// Freír las empanadas.
console.log("¡Es hora de freír!");
console.log("7. Calienta suficiente aceite en una sartén a fuego medio.");
console.log("8. Coloca las empanadas en el aceite caliente y fríe hasta que estén doradas.");
console.log("9. Retira las empanadas y colócalas en papel absorbente.");
console.log("¡Empanadas listas para disfrutar!");

// Comentario adicional sobre el ingrediente secreto.s
